const getWidthInMeter = (value) => {
  return C_TABLE_OPENING.width[value] 
}

const getHeightInMeter = (value) => {
  return C_TABLE_OPENING.height[value] 
}

const getDimension = (width, height) => {
  let width_string = String(width);
  let height_string = String(height);
  if (width_string in C_TABLE_DIMENSION) {

    if (height_string in C_TABLE_DIMENSION[width_string]){
      return C_TABLE_DIMENSION[width_string][height_string]
    }else{
      return getWidthInMeter(width) * getHeightInMeter(height)
    }

  }else{
    return getWidthInMeter(width) * getHeightInMeter(height)
  }
}


const getAreaOpening  = (width, height) => {
  return -getDimension(width, height)
}

